-- ----------------------------
-- View structure for STUDENT_SCORE_REPORT
-- ----------------------------
CREATE OR REPLACE VIEW "QLSV"."STUDENT_SCORE_REPORT" AS SELECT 
    s.ID as student_id,
    s.NAME as student_name,
    sub.NAME as subject_name,
    ss.SCORE_AVERAGE,
    CASE 
        WHEN ss.SCORE_AVERAGE >= 5 THEN 'Qua môn'
        ELSE 'Học lại'
    END as is_passed,
    c.NAME as cycle_name
FROM STUDENT s
JOIN SCORE_SUBJECT ss ON s.ID = ss.STUDENT_ID
JOIN SUBJECT sub ON ss.SUBJECT_ID = sub.ID
JOIN CYCLE c ON sub.CYCLE_ID = c.ID
WHERE s.DELETED = 0 AND ss.DELETED = 0;

