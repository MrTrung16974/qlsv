CREATE TRIGGER "QLSV"."CALCULATE_SCORE_AVERAGE" BEFORE UPDATE ON "QLSV"."SCORE_SUBJECT" REFERENCING OLD AS "OLD" NEW AS "NEW" FOR EACH ROW DISABLE 
BEGIN
    DECLARE process_coef DECIMAL(4,2);
    DECLARE exam_coef DECIMAL(4,2);
    
    SELECT PROCESS_COEFFICIENT, EXAM_COEFFICIENT 
    INTO process_coef, exam_coef
    FROM SUBJECT 
    WHERE ID = NEW.SUBJECT_ID;
    
    IF NEW.SCORE_PROCESS IS NOT NULL AND NEW.SCORE_FINAL IS NOT NULL THEN
        SET NEW.SCORE_AVERAGE = NEW.SCORE_PROCESS * process_coef + NEW.SCORE_FINAL * exam_coef;
    END IF;
END//
